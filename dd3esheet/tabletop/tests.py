class _Base(TestCase):
    def setUp(self):
        self.owner = User.objects.create_user('owner', password='x' * 12)
        self.stranger = User.objects.create_user('stranger', password='x' * 12)
        self.table = GameTable.objects.create(Owner=self.owner, Name='Mesa')
        self.map = Map.objects.create(Table=self.table, Name='Cena', GridMode=Map.SQUARE, GridSize=64)
        self.table.ActiveMap = self.map
        self.table.save()

    def url(self, name, *args):
        return reverse('tabletop:' + name, args=[self.table.Slug, *args])


class PermissionTests(_Base):
    def test_table_view_public(self):
        resp = self.client.get(self.url('table'))
        self.assertEqual(resp.status_code, 200)
        self.assertContains(resp, 'Mesa')

    def test_manage_requires_owner(self):
        self.assertEqual(self.client.get(self.url('manage')).status_code, 403)  # anônimo
        self.client.force_login(self.stranger)
        self.assertEqual(self.client.get(self.url('manage')).status_code, 403)
        self.client.force_login(self.owner)
        self.assertEqual(self.client.get(self.url('manage')).status_code, 200)

    def test_add_map_owner_only(self):
        self.assertEqual(self.client.post(self.url('add-map'), {'Name': 'X'}).status_code, 403)
        self.client.force_login(self.owner)
        self.client.post(self.url('add-map'), {'Name': 'Nova', 'GridMode': 'free'})
        self.assertTrue(Map.objects.filter(Table=self.table, Name='Nova').exists())

    def test_set_active_switches_scene(self):
        self.client.force_login(self.owner)
        other = Map.objects.create(Table=self.table, Name='Outra')
        self.client.post(self.url('set-active', other.id))
        self.table.refresh_from_db()
        self.assertEqual(self.table.ActiveMap_id, other.id)


