@override_settings(MEDIA_ROOT=tempfile.mkdtemp())
class UploadTests(_Base):
    def test_add_token_with_upload_creates_sprite(self):
        self.client.force_login(self.owner)
        img = SimpleUploadedFile('goblin.png', PNG_1x1, content_type='image/png')
        self.client.post(self.url('add-token'), {
            'MapId': self.map.id, 'Kind': 'enemy', 'Label': 'Bicho', 'SpriteFile': img,
        })
        tok = Token.objects.get(Map=self.map, Label='Bicho')
        self.assertIsNotNone(tok.SpriteAsset)
        self.assertEqual(tok.SpriteAsset.Category, SpriteAsset.MAP_TOKEN)
        self.assertEqual(tok.SpriteAsset.Owner_id, self.owner.id)

    def test_svg_upload_is_rejected(self):
        """SVG carrega <script> → não pode virar SpriteAsset servido na mesma origem."""
        self.client.force_login(self.owner)
        svg = SimpleUploadedFile(
            'evil.svg',
            b'<svg xmlns="http://www.w3.org/2000/svg"><script>alert(1)</script></svg>',
            content_type='image/svg+xml',
        )
        self.client.post(self.url('add-token'), {
            'MapId': self.map.id, 'Kind': 'enemy', 'Label': 'Xss', 'SpriteFile': svg,
        })
        tok = Token.objects.get(Map=self.map, Label='Xss')
        self.assertIsNone(tok.SpriteAsset)                      # upload recusado
        self.assertFalse(SpriteAsset.objects.filter(Name='Xss').exists())
